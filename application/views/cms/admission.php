	<h2>Admission Notice</h2>
	<p>It has been decided by the Admission Committee of the College that the counselling for admission to the B.Ed. Course (session 2014-15) will be held on 11-08-2014 at 11 a.m. candidates who had reported in the office from 25.06.2014 to 02.08.2014 are requested to be present in the college premises with all original testimonials.</p>

	<h2>Admission Process</h2>
	<p>The last dates of issuing application forms and receiving the forms duly filled in will be focussed in the advertisement on the daily News paper as well as on the college notice board.</p>
	<p>An admission committee with Teacher-in-charge in the chair and representatives of teachers and N.T.S. as members will be formed as per direction of the administrator of the college. The committee will prepare the list of the candidates, will monitor the counselling session and will perform the process of admission under the leadership of the Teacher-in-charge of the college. This provision may be changed as per direction of the affiliating University.</p>
	<p>Admission of teachers deputed from Secondary Schools affiliated to W.B.B.S.E./W.B.B.O.M.F. will be guided by years of approved services in Secondary schools / Madrasha. In case of applicants having equal length of approved service in Secondary Schools, preference will be given to academic qualifications. In case of period of service and academic qualification both are equal, selection will be done on the basis of % Marks obtained by the candidate in different examinations. </p>
	<p>Admission of SC, ST, OBC, PH students will be made as per the latest order of the U.G.C. and the State Government. The division of these seats will be made as per the decision of the admission committee.</p>

